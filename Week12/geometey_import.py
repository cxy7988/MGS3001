import math
def circum(radius):
    circum = 2 * math.pi * radius
    return circum


def circle_area(radius):
    area = math.pi * radius ** 2
    return area


def peri(weight, height):
    peri1 = 2*(weight + height)
    return peri1


def rectanggle_area(weight, height):
    area1 = weight * height
    return area1